function FloatingSkillShortCutWnd_1::onMouseEnter(%this)
{
	FloatingSkillShortCutWnd_1_BackgroundImage.setvisible(1);
}

function FloatingSkillShortCutWnd_1::onMouseLeave(%this)
{
	FloatingSkillShortCutWnd_1_BackgroundImage.setvisible(0);
}

function FloatingSkillShortCutWnd_1::onRightMouseDown(%this)
{
	FloatShortCut_ControlWnd.setposition(getword(FloatingSkillShortCutWnd_1.getposition(),0),getword(FloatingSkillShortCutWnd_1.getposition(),1) + 34);
	OpenFloatShortCut_ControlWnd();
	//Canvas.setTempWnd(FloatShortCut_ControlWnd);
}

function FloatingSkillShortCutWnd_2::onMouseEnter(%this)
{
	FloatingSkillShortCutWnd_2_BackgroundImage.setvisible(1);
}

function FloatingSkillShortCutWnd_2::onMouseLeave(%this)
{
	FloatingSkillShortCutWnd_2_BackgroundImage.setvisible(0);
}

function FloatingSkillShortCutWnd_2::onRightMouseDown(%this)
{
	FloatShortCut_ControlWnd.setposition(getword(FloatingSkillShortCutWnd_2.getposition(),0) + 34,getword(FloatingSkillShortCutWnd_2.getposition(),1));
	OpenFloatShortCut_ControlWnd();
	//Canvas.setTempWnd(FloatShortCut_ControlWnd);
}

function FloatingSkillShortCutWnd_3::onMouseEnter(%this)
{
	FloatingSkillShortCutWnd_3_BackgroundImage.setvisible(1);
}

function FloatingSkillShortCutWnd_3::onMouseLeave(%this)
{
	FloatingSkillShortCutWnd_3_BackgroundImage.setvisible(0);
}

function FloatingSkillShortCutWnd_3::onRightMouseDown(%this)
{
	FloatShortCut_ControlWnd.setposition(getword(FloatingSkillShortCutWnd_3.getposition(),0) + 141,getword(FloatingSkillShortCutWnd_3.getposition(),1));
	OpenFloatShortCut_ControlWnd();
	//Canvas.setTempWnd(FloatShortCut_ControlWnd);
}

function IsLockFloatingShortCutWnd()
{
	if(ControlWnd_CheckBox1.isstateon())
	{
		FloatingSkillShortCutWnd_1.MoveHotspot = "1 1 0 0";
		FloatingSkillShortCutWnd_2.MoveHotspot = "1 1 0 0";
		FloatingSkillShortCutWnd_3.MoveHotspot = "1 1 0 0";
	}
	else
	{
		FloatingSkillShortCutWnd_1.MoveHotspot = "0 0 1024 768";
		FloatingSkillShortCutWnd_2.MoveHotspot = "0 0 1024 768";
		FloatingSkillShortCutWnd_3.MoveHotspot = "0 0 1024 768";
	}
	CloseFloatShortCut_controlWnd();
}
$Floating_TempNum = 1;
function OpenFloating(%index)
{
	switch(%index)
	{
		case 1:
			FloatingSkillShortCutWnd_1.setvisible(1);
			FloatingSkillShortCutWnd_2.setvisible(0);
			FloatingSkillShortCutWnd_3.setvisible(0);
		case 2:
		  FloatingSkillShortCutWnd_1.setvisible(0);
			FloatingSkillShortCutWnd_2.setvisible(1);
			FloatingSkillShortCutWnd_3.setvisible(0);
		case 3:
		  FloatingSkillShortCutWnd_1.setvisible(0);
			FloatingSkillShortCutWnd_2.setvisible(0);
			FloatingSkillShortCutWnd_3.setvisible(1);
	}
	CloseFloatShortCut_controlWnd();
	("FloatingSkillShortCutWnd_" @ %index).setposition(getword(("FloatingSkillShortCutWnd_" @ $Floating_TempNum).getposition(),0),getword(("FloatingSkillShortCutWnd_" @ $Floating_TempNum).getposition(),1));
	$Floating_TempNum = %index;
}

function OpenFloatShortCut_ControlWnd()
{
	Canvas.setTempWnd(FloatShortCut_ControlWnd);
	FloatShortCut_ControlWnd.setvisible(1);
}

function CloseFloatShortCut_controlWnd()
{
	FloatShortCut_ControlWnd.setvisible(0);
	Canvas.setTempWnd(FloatShortCut_ControlWnd);
}

