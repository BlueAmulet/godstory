//任务结束，奖励与设置信息
function AddMissionReward(%Player, %Mid, %Param)
{
//	echo("AddMissionReward ＝ ");

	%PlayerID          = %Player.GetPlayerID();
	%MissionData       = "MissionData_" @ %Mid;
	%MissionRewardData = "MissionRewardData_" @ %Mid;

	//扣除任务获取道具
	for(%i = 0; %i < 9; %i++)
	{
		%ItemGet = %Player.GetMissionFlag(%Mid, 2100 + %i);
		%GetNum  = %Player.GetMissionFlag(%Mid, 2200 + %i);

//		echo("扣除任务获取道具= "@%i@" = ["@%ItemGet@"] = ["@%GetNum@"]");

		if( (%ItemGet > 0)&&(%GetNum > 0) )
			%Player.PutItem(%ItemGet,- %GetNum);
		else
			break;
//			DelItemFromInventory(%PlayerID, %ItemGet, %GetNum);
	}

	//扣除接任务时给的道具
	if(%MissionData.ItemAdd !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%ItemAdd    = GetWord(%MissionData.ItemAdd,    %i * 2);
			%AddNum     = GetWord(%MissionData.ItemAdd,    %i * 2 + 1);
			%ItemDelete = GetWord(%MissionData.ItemDelete, 0);

//  			echo("扣除接任务时给的道具= "@%i@" = ["@%ItemAdd@"] = ["@%AddNum@"]");

			if( (%ItemAdd $= "")&&(%AddNum $= "") )
				break;

			if(%ItemDelete == 1)
				%Player.PutItem(%ItemAdd,- %AddNum);
//				DelItemFromInventory(%PlayerID, %ItemAdd, %AddNum);
		}

	//扣除任务使用道具
	if(%MissionData.ItemUse !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%ItemUse = GetWord(%MissionData.ItemUse, %i * 2);
			%UseNum  = GetWord(%MissionData.ItemUse, %i * 2 + 1);

//			echo("扣除任务使用道具= "@%i@" = ["@%ItemUse@"] = ["@%UseNum@"]");

			if( (%ItemUse $= "")&&(%UseNum $= "") )
				break;
			else
				%Player.PutItem(%ItemUse,- %UseNum);
//				DelItemFromInventory(%PlayerID, %ItemUse, %UseNum);
		}

	//给选择性道具
	if(%MissionRewardData.ItemChose !$= "0")
		{
			%Set = 0;

			//判断性别限制
			if( (%Mid == 10103)||(%Mid == 10106)||(%Mid == 10163) )
				if(%Player.GetSex() == 2)
					%Set = 3;

      //判断门宗限制
      if(%Mid == 10127)
				{
					if(%Player.GetFamily() == 1){%Set = 0;}
					if(%Player.GetFamily() == 2){%Set = 2;}
					if(%Player.GetFamily() == 3){%Set = 4;}
					if(%Player.GetFamily() == 4){%Set = 6;}
					if(%Player.GetFamily() == 5){%Set = 8;}
					if(%Player.GetFamily() == 6){%Set = 10;}
					if(%Player.GetFamily() == 7){%Set = 12;}
					if(%Player.GetFamily() == 8){%Set = 14;}

				}

			%ItemChose = GetWord(%MissionRewardData.ItemChose, (%Param + %Set) * 2);
			%ChoseNum  = GetWord(%MissionRewardData.ItemChose, (%Param + %Set) * 2 + 1);

//			echo("给选择性道具= "@%i@" = ["@%ItemChose@"] = ["@%ChoseNum@"]");

			if( (%ItemChose !$= "")&&(%ChoseNum !$= "") )
				%Player.PutItem(%ItemChose,%ChoseNum);
//				AddItemToInventory(%PlayerID, %ItemChose, %ChoseNum);
		}

	//必给的道具
	if(%MissionRewardData.ItemSet !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%ItemSet = GetWord(%MissionRewardData.ItemSet, %i * 2);
			%SetNum  = GetWord(%MissionRewardData.ItemSet, %i * 2 + 1);

//			echo("必给的道具= "@%i@" = ["@%ItemSet@"] = ["@%SetNum@"]");

			if( (%ItemSet $= "")&&(%SetNum $= "") )
				break;
			else
				%Player.PutItem(%ItemSet,%SetNum);
//				AddItemToInventory(%PlayerID, %ItemSet, %SetNum);
		}

	//增减道具，一次性操作
	%MissionItem = %Player.AddItem();
	if(!%MissionItem)
		{
			echo("物品数据错误！任务编号："@%Mid);
			return;
		}

	//给经验
//	echo("给经验 ＝ "@%MissionRewardData.Exp );
	if(%MissionRewardData.Exp > 0)
		%Player.AddExp( %MissionRewardData.Exp );

	//10级以内玩家自动升级
	if(%Player.GetLevel() < 10)
		if(%Player.GetExp() >= %Player.GetLevelExp())
			{
				%Player.AddExp(- %Player.GetLevelExp());
				%Player.AddLevel(1);
			}

	//给金钱
	if(%MissionRewardData.Money > 0)
		%Player.AddMoney( %MissionRewardData.Money , 1);
	//给金元
	if(%MissionRewardData.Yuan > 0)
		%Player.AddMoney( %MissionRewardData.Yuan , 2);
	//给元宝

	//给积分

	//给声望

	//给技能
	if(%MissionRewardData.Skill !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%Skill = GetWord(%MissionRewardData.Skill, %i);

			if(%Skill $= "")
				break;
			else
				%Player.AddSkill(%Skill);
		}

	//给状态
	if(%MissionRewardData.Buff !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%Buff = GetWord(%MissionRewardData.Buff, %i);

			if(%Buff $= "")
				break;
			else
				%Player.AddSkill(%Buff, 0);
		}

	//给特效
	AddEffect(1, %Player, $SP_Effect[2]);

	//设置任务完成
	%Player.SetMission(%Mid,1);

	//任务完成附加设置

	//学习生活技能
	if(%Mid == 22101){LearnLivingSkill(%Player,501011001);}//挖矿技能师
	if(%Mid == 22103){LearnLivingSkill(%Player,501031001);}//钓鱼技能师

}

//添加任务数据
function AddMissionData(%Mid, %PreMid, %Name, %Map, %Lv, %NpcA, %NpcX, %NpcZ, %ItemAdd, %ItemDelete, %MonsterKill, %ItemGet, %ItemUse, %Target, %X_Man)
{
	//拼装任务数据编号
	%DataID = "MissionData_" @ %Mid;
	
	%LvA     =  GetWord(%Lv, 0);			//接任务等级
	%LvZ     =  GetWord(%Lv, 1);			//交任务等级
	%JianYi  =  GetWord(%Lv, 2);			//任务建议等级
	%SeeA    =  GetWord(%Lv, 3);			//任务最小可视等级
	%SeeZ    =  GetWord(%Lv, 4);			//任务最大可视等级
	
	//创建任务数据
	new scriptObject(%DataID)
		{
			Mid         =  %Mid;        	//编号
			PreMid      =  %PreMid;     	//前置任务
			Name        =  %Name;       	//名称
			Map         =  %Map;        	//归属地图
			LvA         =  %LvA;         	//接任务等级
			LvZ         =  %LvZ;         	//交任务等级
			JianYi			=  %JianYi;				//任务建议等级
			SeeA				=  %SeeA;					//任务最小可视等级
			SeeZ				=  %SeeZ;					//任务最大可视等级
			NpcA        =  %NpcA;       	//起始NPC
			NpcX        =  %NpcX;       	//中途对话NPC
			NpcZ        =  %NpcZ;       	//结束NPC
			ItemAdd     =  %ItemAdd;    	//接受任务时给于的任务道具
			ItemDelete  =  %ItemDelete;   //交付时是否要扣除
			MonsterKill =  %MonsterKill;	//杀怪
			ItemGet     =  %ItemGet;    	//取物
			ItemUse     =  %ItemUse;    	//使用道具
			Target      =  %Target;     	//目标
			X_Man       =  %X_Man;     		//变身状态
		};

	//任务总数增加1
	$MissionCount++;

	//任务数据相应位设置任务Data数据
	$MissionData[$MissionCount] = %DataID;

	//添加至相应等级的任务编号数组中
	$MissionData_Lv[%LvA, $MissionData_Lv[%LvA]++ ] = %Mid;
//	if(strlen(%LvA) == 1){%LvA = "00" @ %LvA;}
//	if(strlen(%LvA) == 2){%LvA = "0" @ %LvA;}
//	eval("$MissionData_Lv_" @ %LvA @ "[$MissionData_Lv_" @ %LvA @ "++] = %Mid;");

	//添加至相应可视等级的任务编号数组中
	for(%i = %SeeA;%i < %SeeZ;%i++)
		$MissionData_See_Lv[%i, $MissionData_See_Lv[%i]++ ] = %Mid;
	
	//添加至相应地图编号的任务编号数组中
	$MissionData_Map[%Map, $MissionData_Map[%Map]++ ] = %Mid;
//	eval("$MissionData_Map_" @ %Map @ "[$MissionData_Map_" @ %Map @ "++] = %Mid;");

	//添加至相应起始NpcA编号的任务编号数组中
//			eval("$MissionData_NpcA_" @ %NpcAID @ "[$MissionData_NpcA_" @ %NpcAID @ "++] = %Mid;");
	for(%i = 0; %i < 9; %i++)
	{
		%NpcAID = GetWord(%NpcA,%i);
		if(%NpcAID !$= "")
			$MissionData_NpcA[%NpcAID, $MissionData_NpcA[%NpcAID]++ ] = %Mid;
		else
			break;
	}

	//添加至相应中途对话NpcX编号的任务编号数组中
//			eval("$MissionData_NpcX_" @ %NpcXID @ "[$MissionData_NpcX_" @ %NpcXID @ "++] = %Mid;");
	for(%i = 0; %i < 9; %i++)
	{
		%NpcXID = GetWord(%NpcX,%i);
		if(%NpcXID !$= "")
			$MissionData_NpcX[%NpcXID, $MissionData_NpcX[%NpcXID]++ ] = %Mid;
		else
			break;
	}

	//添加至相应结束NpcZ编号的任务编号数组中
//			eval("$MissionData_NpcZ_" @ %NpcZID @ "[$MissionData_NpcZ_" @ %NpcZID @ "++] = %Mid;");
	for(%i = 0; %i < 9; %i++)
	{
		%NpcZID = GetWord(%NpcZ,%i);
		if(%NpcZID !$= "")
			$MissionData_NpcZ[%NpcZID, $MissionData_NpcZ[%NpcZID]++ ] = %Mid;
		else
			break;
	}

	//添加至相应怪物编号的任务编号数组中
//			eval("$MissionData_MonsterKill_" @ %MonsterID @ "[$MissionData_MonsterKill_" @ %MonsterID @ "++] = %Mid;");
	for(%i = 0; %i < 30; %i++)
	{
		%MonsterID = GetWord(%MonsterKill,%i * 2);
		if(%MonsterID > 0)
			$MissionData_MonsterKill[%MonsterID, $MissionData_MonsterKill[%MonsterID]++ ] = %Mid;
		else
			break;
	}

	//添加至相应道具获取编号的任务编号数组中
//			eval("$MissionData_ItemGet_" @ %ItemGetID @ "[$MissionData_ItemGet_" @ %ItemGetID @ "++] = %Mid;");
	for(%i = 0; %i < 30; %i++)
	{
		%ItemGetID = GetWord(%ItemGet,%i * 2);
		if(%ItemGetID > 0)
			$MissionData_ItemGet[%ItemGetID, $MissionData_ItemGet[%ItemGetID]++ ] = %Mid;
		else
			break;
	}

	//添加至相应道具使用编号的任务编号数组中
//				eval("$MissionData_ItemUse_" @ %ItemUseID @ "[$MissionData_ItemUse_" @ %ItemUseID @ "++] = %Mid;");
	for(%i = 0; %i < 30; %i++)
	{
		%ItemUseID = GetWord(%ItemUse,%i * 2);
		if(%ItemUseID > 0)
			{
				$MissionData_ItemUse[%ItemUseID, $MissionData_ItemUse[%ItemUseID]++ ] = %Mid;

//				//添加此道具的使用目标
//				for(%ii = 0; %ii < 30; %ii++)
//				{
//					%TargetID = GetWord(%Target,%ii * 2);
//					if(%TargetID > 0)
//						eval("$MissionData_ItemUse_" @ %ItemUseID @ "_Target[%ii + 1] = %TargetID;");
//					else
//						break;
//				}
			}
		else
			break;
	}
}

