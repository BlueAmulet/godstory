//任务接受，设置基础信息
function AddMissionAccepted(%Player, %Mid)
{
	%MissionData = "MissionData_" @ %Mid;

	%Player.SetMissionFlag(%Mid,1,1); //设置任务接受

	if(%Mid == 10749)
		%Player.SetFlagsByte(10,0);
//		
//	if(%Mid == 10000)
//		{
//			%Player.SetMissionFlag(%Mid, 9000, 10);	       //设置,怪物总数
//			%Player.SetMissionFlag(%Mid, 9100, 0); 				//设置,怪物当前数量
//    }
//    
	//设置是否需要杀怪
	if(%MissionData.MonsterKill !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%Monster = GetWord(%MissionData.MonsterKill, %i * 2);
			%KillNum = GetWord(%MissionData.MonsterKill, %i * 2 + 1);

			if( (%Monster $= "")&&(%KillNum $= "") )
				break;
			else
				{
					%Player.SetMissionFlag(%Mid, 3100 + %i, %Monster);	//设置,怪物编号
					%Player.SetMissionFlag(%Mid, 3200 + %i, %KillNum);	//设置,怪物总数
					%Player.SetMissionFlag(%Mid, 3300 + %i, 0); 				//设置,怪物当前数量
				}
		}

	//设置是否需要使用道具触发的目标
	if(%MissionData.Target !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%Target = GetWord(%MissionData.Target, %i * 2);
			%TarNum = GetWord(%MissionData.Target, %i * 2 + 1);

			if( (%Target $= "")&&(%TarNum $= "") )
				break;
			else
				{
					%Player.SetMissionFlag(%Mid, 1100 + %i, %Target);		//设置,目标编号
					%Player.SetMissionFlag(%Mid, 1200 + %i, %TarNum);		//设置,目标触发总数
					%Player.SetMissionFlag(%Mid, 1300 + %i, 0); 				//设置,目标触发当前数量
				}
		}

	//设置中途是否需要寻找目标
	if(%MissionData.NpcX !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%NpcX    = GetWord(%MissionData.NpcX, %i * 2);
			%NpcXNum = GetWord(%MissionData.NpcX, %i * 2 + 1);

			if( (%NpcX $= "")&&(%NpcXNum $= "") )
				break;
			else
				{
					%Player.SetMissionFlag(%Mid, 1150 + %i, %NpcX);			//设置,目标编号
					%Player.SetMissionFlag(%Mid, 1250 + %i, %NpcXNum);	//设置,目标触发总数
					%Player.SetMissionFlag(%Mid, 1350 + %i, 0); 				//设置,目标触发当前数量
				}
		}

	//设置是否需要获取物品
	if(%MissionData.ItemGet !$= "0")
		for(%i = 0; %i < 9; %i++)
		{
			%ItemGet = GetWord(%MissionData.ItemGet, %i * 2);
			%GetNum  = GetWord(%MissionData.ItemGet, %i * 2 + 1);

			if( (%ItemGet $= "")&&(%GetNum $= "") )
				break;
			else
				{
					if(%Player.GetItemCount(%ItemGet) > %GetNum)
						%NowNum  = %GetNum;
					else
						%NowNum  = %Player.GetItemCount(%ItemGet);

					%Player.SetMissionFlag(%Mid, 2100 + %i, %ItemGet);	//设置,ITEM编号
					%Player.SetMissionFlag(%Mid, 2200 + %i, %GetNum);		//设置,ITEM获取总数
					%Player.SetMissionFlag(%Mid, 2300 + %i, %NowNum); 	//设置,ITEM获取当前数量
				}
		}

	//设置是否需要运输物品
	if(%MissionData.ItemDelete !$= "0")
		if(%MissionData.ItemAdd !$= "0")
			for(%i = 0; %i < 9; %i++)
			{
				%ItemAdd = GetWord(%MissionData.ItemAdd, %i * 2);
				%AddNum  = GetWord(%MissionData.ItemAdd, %i * 2 + 1);

				if( (%ItemAdd $= "")&&(%AddNum $= "") )
					break;
				else
					{
						if(%Player.GetItemCount(%ItemAdd) > %AddNum)
							%NowNum  = %AddNum;
						else
							%NowNum  = %Player.GetItemCount(%ItemAdd);

						%Player.SetMissionFlag(%Mid, 2150 + %i, %ItemAdd);	//设置,ITEM编号
						%Player.SetMissionFlag(%Mid, 2250 + %i, %AddNum);		//设置,ITEM运输总数
						%Player.SetMissionFlag(%Mid, 2350 + %i, %NowNum);		//设置,ITEM运输当前数量
					}
			}

//	//设置是否需要使用物品
//	if(%MissionData.ItemUse !$= "0")
//		for(%i = 0; %i < 9; %i++)
//		{
//			%ItemUse = GetWord(%MissionData.ItemUse, %i * 2);
//			%UseNum  = GetWord(%MissionData.ItemUse, %i * 2 + 1);
//
//			if( (%ItemUse $= "")&&(%UseNum $= "") )
//				break;
//			else
//				{
//					%Player.SetMissionFlag(%Mid, 2400 + %i, %ItemUse);	//设置,ITEM编号
//					%Player.SetMissionFlag(%Mid, 2500 + %i, %UseNum);		//设置,ITEM使用总数
//					%Player.SetMissionFlag(%Mid, 2600 + %i, 0); 				//设置,ITEM使用当前数量
//				}
//		}

	%Player.SetMissionFlag(%Mid, 4000,10002); //设置,对话编号
//	%Player.SetMissionFlag(%Mid,5000,); //设置,技能编号
//	%Player.SetMissionFlag(%Mid,6000,); //设置,状态编号
//	%Player.SetMissionFlag(%Mid,7000,); //设置,时间
	
	//纯粹数值计数
	%Step8X_Min = 0;
	%Step8X_Max = 0;
	
			if(%Mid == 10000){%Step8X_Min = %Player.GetLevel();%Step8X_Max = 10;}														//第一个任务-修仙之旅
			if(%Mid == 22002){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501031001);%Step8X_Max = 1;}	//钓鱼		//生活技能任务
			if(%Mid == 22004){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501061001);%Step8X_Max = 1;}	//狩猎
			if(%Mid == 22006){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501051001);%Step8X_Max = 1;}	//采药
			if(%Mid == 22008){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501041001);%Step8X_Max = 1;}	//种植
			if(%Mid == 22010){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501021001);%Step8X_Max = 1;}	//伐木
			if(%Mid == 22012){%Step8X_Min = isLearnLivingSkill(%Player.GetPlayerID(), 501011001);%Step8X_Max = 1;}	//采矿	
		
	if(%Step8X_Max > 0)
		{
			%Player.SetMissionFlag(%Mid, 8100, %Step8X_Min); //设置,当前数值
			%Player.SetMissionFlag(%Mid, 8000, %Step8X_Max); //设置,最大数值
		}
		
	//文字描述	
	%WenZi_Min = 0;	
	
	if(%Mid == 10001){%WenZi_Min = 1;}
		
	if(%WenZi_Min > 0)
		{
